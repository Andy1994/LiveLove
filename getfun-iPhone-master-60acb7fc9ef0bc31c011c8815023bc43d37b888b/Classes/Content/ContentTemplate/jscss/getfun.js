define("./scripts/model", function(require, exports) {
    require("./scripts/base");
    var default_avatar = "./image/default_avatar.png";
    function filterDetail(data) {
        var avatar;
        if (!data.user.avatar || data.user.avatar.length == 0) {
            avatar = default_avatar;
        } else {
            avatar = data.user.avatar + "?imageView2/1/w/40/h/40";
        }
        data.user.avatar = avatar;
        data.user.time = $.J_time(data.content.createTime);
        var marks = [];
        var count = 0;
        var l = 0;
        var tags = data.tags;
        if (data.content.type == "FORWARD") {
            tags = data.relatedContent.tags;
        }
        for (var i in tags) {
            count += +tags[i]["name"].length;
            if (count >= 30 || l >= 3) {
            } else {
                marks.push({
                    "mark": tags[i]
                });
            }
            l++;
        }
        data.contentDetail.title = decodeHtml(data.contentDetail.title);
        data.contentDetail.urlTitle = decodeHtml(data.contentDetail.urlTitle);
        if (data.content.type == "FORWARD") {
            data.relatedContent.contentDetail.title = decodeHtml(data.relatedContent.contentDetail.title);
            data.relatedContent.contentDetail.urlTitle = decodeHtml(data.relatedContent.contentDetail.urlTitle);
        }
        data.marks = marks;
        if (HOST.android && data.user.titles && data.user.titles.length > 0) {
            data.user.title = {
                "name": decodeHtml(data.user.titles[0].title.name),
                "iconUrl": data.user.titles[0].title.iconUrl
            };
        }
    }
    function decodeHtml(str) {
        if (str == null) {
            return null;
        }
        var s = "";
        if (str.length == 0) return "";
        s = str.replace(/&amp;/g, "&");
        s = s.replace(/&lt;/g, "<");
        s = s.replace(/&gt;/g, ">");
        s = s.replace(/&nbsp;/g, " ");
        s = s.replace(/&#39;/g, "'");
        s = s.replace(/&quot;/g, '"');
        return s;
    }
    return {
        "filterDetail": filterDetail
    };
});
/**
 * 功能:图片惰性加载。在需要图片惰性加载的页面底部载入。再将图片加上
 * class=“lazy-img”。并将图片的真实地址放置在图片的自定义属性 dataimg 中。
 *
 * 应用 img lazy load 前：
 *      <img  src="{img.url}">
 *
 * 应用 img lazy load 后：
 *      <img class="lazy-img" dataimg="{img.url}" src="{GREY_IMG_URL}">
 *
 */
define("./scripts/img.lazyload", function(require, exports, module) {
    // var GREY_IMG_URL = 'http://shared.ydstatic.com/sw/grey.png';
    var INIT = false;
    //是否初始化
    var _nativeScrollOffset = 0;
    //native滚动过的距离
    var _nativeViewHeight = 0;
    //native可视区域的高度
    var downloadImg;
    function getOimgUrl(surl) {
        return surl;
    }
    var ImgLazyLoad = {
        "init": function() {
            var that = this;
            // 图片真实地址存放的自定义属性
            that.srcStore = "dataimg";
            // 惰性加载的图片需要添加的class
            that.className = "lazy-img";
            // 该值越小，惰性越强（加载越少）
            that.sensitivity = 300;
            that.updateImgList();
            var _timeHandler;
            $(window).on("scroll", onscroll);
            function onscroll() {
                that.changeimg();
            }
            that.changeimg();
        },
        "updateImgList": function() {
            this.imglist = $("img." + this.className);
        },
        "loadYesOrno": function(img) {
            var s = this.sensitivity;
            //300 提前300加载
            var ioffset = img.offset();
            // 图片的上下边界
            var it = ioffset.top;
            //图片上边距到顶部的距离
            var ib = ioffset.height + it;
            //图片下边距到顶部的距离
            // 窗口可视区域的上下边界
            var wt = $(window).scrollTop();
            //当前窗口的上边距到顶部的距离
            var wb = $(window).height() + wt;
            //当前窗口的下边距到顶部的距离。
            // 图片的上下边界在可视范围内时，返回 true，否则返回 false
            var result = it >= wt && it - s <= wb || ib >= wt && ib <= wb || it < wt && ib > wb;
            return result;
        },
        "loadImg": function(img, index) {
            var that = this;
            img[0].onload || (img[0].onload = function() {
                $(this).removeClass(that.className).removeAttr(that.srcStore);
                that.imglist[index] = false;
                //为false说明是该图片加载完成，不需要再加载了
                this.onerror = this.onload = null;
            });
            var imgUrl = img.attr(that.srcStore);
            img.attr("src", getOimgUrl(imgUrl));
        },
        "changeimg": function() {
            var that = this;
            if (!that.imglist.length) return;
            that.imglist.each(function(index, val) {
                if (!val) return;
                var img = $(val);
                if (!that.loadYesOrno(img)) return;
                if (!img.attr(that.srcStore)) return;
                if (!that.imglist[index]) return;
                that.loadImg(img, index);
            });
        }
    };
    // -------------------------------------------------------------------------
    // YDNN.ImageLazyLoad = {};
    // YDNN.ImageLazyLoad.init = function () {
    //     ImgLazyLoad.init();
    // };
    module.exports = function(opt) {
        if (!INIT) {
            $(function() {
                ImgLazyLoad.init.apply(ImgLazyLoad);
                INIT = true;
            });
        } else {
            ImgLazyLoad.updateImgList();
            ImgLazyLoad.changeimg();
        }
    };
});

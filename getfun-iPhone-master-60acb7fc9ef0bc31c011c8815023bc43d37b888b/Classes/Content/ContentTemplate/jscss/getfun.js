define("./scripts/external/bridge_ios", function(require, exports, module) {
    function init(callback) {
        connectWebViewJavascriptBridge(function(b) {
            b.init(function(message, responseCallback) {
                var data = {
                    "JavaScript Response": "Wee!"
                };
                responseCallback(data);
            });
            window._bridge_ = b;
            callback && callback(b);
        });
    }
    function connectWebViewJavascriptBridge(callback) {
        if (window.WebViewJavascriptBridge) {
            callback(WebViewJavascriptBridge);
        } else {
            document.addEventListener("WebViewJavascriptBridgeReady", function() {
                callback(WebViewJavascriptBridge);
            }, false);
        }
    }
    //js调用客户端
    function jsCallClient(methodName, data, callback) {
        window._bridge_ && window._bridge_.callHandler(methodName, data.req, function(responseCallback) {
            callback && callback(responseCallback);
            return;
        });
    }
    //客户端调用js
    function clicntCallJs(methodName, callback) {
        window._bridge_ && window._bridge_.registerHandler(methodName, function(data, responseCallback) {
            callback(data, responseCallback);
        });
    }
    module.exports = {
        "init": init,
        "jsCallClient": jsCallClient,
        "clicntCallJs": clicntCallJs
    };
});
